package com.example.proyecto_login;

public class User {

    public String fullName, email, age, altura, peso;

    public User(){
    }

    public User(String fullName, String age, String email, String altura, String peso){
        this.fullName=fullName;
        this.age=age;
        this.email=email;
        this.altura=altura;
        this.peso=peso;
    }

}
